#include <iostream>
using namespace std;

int main () {
// +, -, *, / , %
	int i;
	i= 2;
	i++; // adds one
	cout << i << endl;; 
		i--; // subtracts one
	cout << i << endl; 

	int b = 12;
	++b; 
	cout << b << endl;

	// system("cls"); //apparently clears the system

	//<, >, <=, >=, !=

	// logical operators used to combine two or more conditions into one 
	// && [and], ||[or], ! [not]

	// = [assign value to], += [assign and add], -= [assign and subtract], *= [assign and multiply], /= [assign and divide], %=
	int x = 5;
	x += 4; // x= x+4
	cout << x << endl;
		x -= 4; // x= x-4
	cout << x << endl;
		x *= 4; // x= x*4
	cout << x << endl;
		x /= 4; // x= x/4
	cout << x << endl;
		x %= 4; // x= x%4
	cout << x << endl;

} 